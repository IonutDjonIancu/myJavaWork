package org.gameloft.ConditionalStatements;

public class Switches
{
    public static void main(String[] args)
    {
        int x = 2;
        switch (x)
        {
            case 0:
                System.out.println("0");
                break;
            case 1:
                System.out.println("1");
                break;
            case 2:
                System.out.println("2");
                break;
            default:
                System.out.println("smth else");
        }




    }
}
